-- 3. Duplicados: mismo teléfono asignado al mismo setter
SELECT
  p.full_name                                          AS setter,
  regexp_replace(l.phone, '\D+', '', 'g')              AS phone_norm,
  count(*)                                             AS copias
FROM public.leads l
JOIN public.profiles p ON p.id = l.assigned_to_user_id
WHERE l.phone IS NOT NULL AND l.phone != ''
GROUP BY p.full_name, regexp_replace(l.phone, '\D+', '', 'g')
HAVING count(*) > 1
ORDER BY copias DESC
LIMIT 20;
