-- 1. Leads visibles por setter (is_closed=false, asignados)
SELECT
  p.full_name                                  AS setter,
  count(*) FILTER (WHERE l.is_closed = false)  AS visibles,
  count(*) FILTER (WHERE l.is_closed = true)   AS ocultos,
  count(*)                                     AS total
FROM public.leads l
JOIN public.profiles p ON p.id = l.assigned_to_user_id
WHERE p.role IN ('setter','admin','mentor')
GROUP BY p.full_name
ORDER BY visibles DESC;

